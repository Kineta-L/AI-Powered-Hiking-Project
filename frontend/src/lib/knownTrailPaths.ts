export function isTigerLeapingGorgeTrail(trail: TrailPathSource) {
  const title = `${trail.titleZh || ''} ${trail.titleEn || ''}`.toLowerCase();
  return title.includes('虎跳峡') || title.includes('tiger leaping gorge') || title.includes('hutiaoxia');
}

export function hasVerifiedTrailPath(trail: TrailPathSource) {
  return Boolean(trail.gpxTrack) || isTigerLeapingGorgeTrail(trail);
}

export function getTrailDisplayPath(trail: TrailPathSource): [number, number][] {
  if (isTigerLeapingGorgeTrail(trail)) return TIGER_LEAPING_GORGE_HIGH_TRAIL;
  if (!trail.gpxTrack) return [];
  return (trail.coordinates || []).map(c => [c.longitude, c.latitude] as [number, number]);
}

export function getTrailCoverImage(trail: TrailPathSource) {
  if (trail.coverImage) return trail.coverImage;

  const title = `${trail.titleZh || ''} ${trail.titleEn || ''} ${trail.region || ''} ${trail.country || ''}`.toLowerCase();
  if (title.includes('kanas') || title.includes('喀纳斯')) {
    return 'https://images.unsplash.com/photo-1470770841072-f978cf4d019e?auto=format&fit=crop&w=900&q=80';
  }
  if (title.includes('grand canyon') || title.includes('大峡谷')) {
    return 'https://images.unsplash.com/photo-1474044159687-1ee9f3a51722?auto=format&fit=crop&w=900&q=80';
  }
  if (title.includes('eiger') || title.includes('switzerland') || title.includes('艾格')) {
    return 'https://images.unsplash.com/photo-1506905925346-21bda4d32df4?auto=format&fit=crop&w=900&q=80';
  }
  if (title.includes('machu') || title.includes('salkantay') || title.includes('inca')) {
    return 'https://images.unsplash.com/photo-1526392060635-9d6019884377?auto=format&fit=crop&w=900&q=80';
  }
  if (title.includes('trolltunga') || title.includes('挪威') || title.includes('norway')) {
    return 'https://images.unsplash.com/photo-1500534314209-a25ddb2bd429?auto=format&fit=crop&w=900&q=80';
  }
  if (title.includes('tiger') || title.includes('虎跳峡') || title.includes('云南')) {
    return 'https://images.unsplash.com/photo-1500530855697-b586d89ba3ee?auto=format&fit=crop&w=900&q=80';
  }
  if (title.includes('贡嘎') || title.includes('gongga') || title.includes('四川')) {
    return 'https://images.unsplash.com/photo-1464822759023-fed622ff2c3b?auto=format&fit=crop&w=900&q=80';
  }
  return 'https://images.unsplash.com/photo-1551632811-561732d1e306?auto=format&fit=crop&w=900&q=80';
}
